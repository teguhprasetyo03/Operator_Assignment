void main(){

    /*
        Operator Assignent adalah operator yang menggunakan
        tanda sama dengan ( = ) untuk mengisi sebuah nilai dalam
        suatu variabel. Operator Assignment digunakan untuk menyatakan
        suatu perintah atau fungsi perhitungan untuk memberikan nilai
        pada variabel dalam bentuk yang disingkat
     */

  int a = 10;
  // Assignment operator penjumlahan
  a+=10;
  print('Nilai dari a = $a');

  int b = 20;
  // Assignment operator pengurangan
  b-=10;
  print('Nilai dari b = $b');

  double c = 20;
  // Assignment operator perkalian
  c*=10;
  print('Nilai dari c = $c');

  double d = 100;
  // Assignment operator pembagian
  d/=25;
  print('Nilai dari d = $d');

  int e = 10;
  // Assignment operator modulus
  e %=7;
  print('Nilai dari e = $e');

}